    public void carga() {
        try {

            con = ManipulaDBC.conectaDB();
            String sql = "SELECT medicos.cedula_profesional, personas.nombre, "
                    + "personas.apellido_paterno, personas.apellido_materno, personas.sexo "
                    + "FROM medicos JOIN personas ON medicos.id_persona=personas.id_persona";

            System.out.println(sql);
            ps = con.prepareStatement(sql);
            res = ps.executeQuery(); //Trae todos los datos de la consulta

            while (res.next()) {

                String nombreCompleto = res.getString("nombre") + " "
                        + res.getString("apellido_paterno") + " "
                        + res.getString("apellido_materno");

                jComboBoxDoctores.addItem(nombreCompleto); //Agrega el nombre al ComboBox*/  
            }

            jComboBoxDoctores.removeItemAt(0);
        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }


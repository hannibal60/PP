    private void jBConsultarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jBConsultarActionPerformed
    {//GEN-HEADEREND:event_jBConsultarActionPerformed
        //aqui empleamos el uso de una variable boolean que evalua si la informacion se contiiene en la base de datos
        q = new Querys();

        if (jTFCurp.getText().length() < 18) {
            Mensaje.error(this, "CURP menor a 18 digitos");
            CtrlInterfaz.selecciona(jTFCurp);

        } else {
            try {
                columnaMap1 = q.Seleccion(con, "*", "datos_paciente", "folio_paciente = '" + jTFCurp.getText().toUpperCase().substring(0, 10) + "'", true);
                columnaMap2 = q.Seleccion(con, "*", "antecedente_paciente", "folio_paciente = '" + jTFCurp.getText().toUpperCase().substring(0, 10) + "'", true);

            } catch (Exception e) {
                System.out.println("Error, hay valores menores a 18  ->" + e);
            }

            if (columnaMap1.isEmpty())//flag = false esto nos indica que no hay registros con esos datos
            {

                flag = false;

                activa(flag);

                carga();

            } else //si flag = true quiere decir que si existen estos datos
            {
                flag = true;

                activa(flag);

            }
        }

        //si flag = true quiere decir que si existen estos datos, en cambio si la flag = false esto nos indica que no hay registros con esos datos 
    }//GEN-LAST:event_jBConsultarActionPerformed


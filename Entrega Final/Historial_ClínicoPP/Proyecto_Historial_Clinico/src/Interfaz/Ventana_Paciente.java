            jTFNomCompPac.setText(jTFNombre.getText() + " " + jTFAPaterno.getText() + " " + jTFAMaterno.getText());
            for (int i = 0; i < 2; i++) {
                if (columnaMap1.get(6).toString().substring(1).equals(jCBSexo.getItemAt(i))) {
                    jCBSexo.setSelectedIndex(i);
                    break;
                }
            }
            for (int i = 0; i < 8; i++) {
                if (columnaMap1.get(7).toString().substring(1).equals(jCBTipoSangre.getItemAt(i))) {
                    jCBTipoSangre.setSelectedIndex(i);
                    break;
                }
            }
            jTFReligion.setText(columnaMap1.get(8).toString().substring(1));
            jTFTelefono.setText(columnaMap1.get(9).toString().substring(1));
            jTFTrabajo.setText(columnaMap1.get(10).toString().substring(1));

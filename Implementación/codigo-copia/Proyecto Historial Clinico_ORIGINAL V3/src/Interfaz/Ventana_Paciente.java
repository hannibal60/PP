    private boolean validaFechaNac(String fecha)
    {
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        formatoFecha.setLenient(false);
        try
        {
            formatoFecha.parse(fecha);
        } catch (ParseException ex)
        {
            return false;
        }
        return true;
    }

    public boolean activa(boolean flag)
    {
        if (flag)
        {
            System.out.println("datos correctos");
            CtrlInterfaz.habilita(false, jTFNomCP, jTFfolio, jTCurp2, jTFFNac, jTFTelefono,
                    jTFfolio, jTFFNac, jTFTelefono,
                    jTFmunicipio, jTFcolonia, jTFcalle, jTFnumero,
                    jTFRel, jTFTra, jTextFieldEdad, jCBsexo, jCBTipSan);
        } else if (flag == false)
        {
            Mensaje.error(this, "No se encontraron registros con estos datos debe agregar un registro");
            System.out.println("datos no encontrados");
            CtrlInterfaz.selecciona(jTFfolio);
            CtrlInterfaz.habilita(true, jTFfolio);
            CtrlInterfaz.habilita(false, jTFNomCP, jTCurp2, jTFFNac, jTFTelefono,
                    jTFFNac, jTFTelefono,
                    jTFmunicipio, jTFcolonia, jTFcalle, jTFnumero,
                    jTFRel, jTFTra, jTextFieldEdad, jCBsexo, jCBTipSan);
        }
        return flag;
    }
    
    
    /**
     * Método para bloquear el pegado de datos en jTextFields
     *
     * @param objs Arreglo no definido de objetos para hacer uso de cajas de
     * texto
     */
    public static void evitarPegar(Object... objs)
    {
        for (Object obj : objs)
        {
            if (obj instanceof JTextField)
            {
                InputMap map2 = ((JTextField) obj).getInputMap(JTextField.WHEN_FOCUSED);
                map2.put(KeyStroke.getKeyStroke(KeyEvent.VK_V, Event.CTRL_MASK), "null");
            }
        }
    }
    
